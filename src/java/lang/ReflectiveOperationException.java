package java.lang;

public class ReflectiveOperationException extends Exception {

	public ReflectiveOperationException() {
		super();
	}

	public ReflectiveOperationException(java.lang.String message) {
		super(message);
	}

	public ReflectiveOperationException(java.lang.String message, java.lang.Throwable cause) {
		super(message, cause);
	}

	public ReflectiveOperationException(java.lang.Throwable cause) {
		super(cause);
	}
}
